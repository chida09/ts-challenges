/**
 * 派生型（Subtype）とは、ある型が別の型の一部であることを示す型関係のことです。
 * すなわち、型Bが型Aの派生型である場合、型Bは型Aの一部であり、型Aが持つすべての性質を引き継いでいるということになります。
 * このとき、型Aはスーパータイプ（Supertype）または親型、型Bはサブタイプ（Subtype）または子型と呼ばれます。
 *
 * 例えば、string型はany型の派生型であり、any型が持つすべての性質を引き継いでいます。
 * 同様に、number[]型はArray<number>型の派生型であり、Array<number>型が持つすべての性質を引き継いでいます。
 */
